#include<bits/stdc++.h>
using namespace std;
// n=90이면 값이 엄청 커지니까 int 대신 long long써야함
long long D[91];

int main() {
    int n;
    cin >> n;
    D[0]=0;
    D[1]=1; D[2]=1; // 1 / 10
    // D[3] 100,101 -> 100 +1 D[3]=D[1]+1=2
    // D[4] 1001,1010,1000-> 1000 +1,+10 D[4]=D[2]+D[1]+1=D[2]+D[3]
    // D[5] 10001 10010 10100 10101 10000-> 10000 +1,+10,+100,+101
    // D[5]=D[3]+D[2]+D[1]+1=D[3]+D[4]
    for (int i=3;i<=n;i++) {
        D[i]=D[i-1]+D[i-2];
    }
    cout << D[n];
}